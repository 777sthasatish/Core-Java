    private void FillDonorCombo() {
        try {
            conn.open();
            String sql = "Select * from tbl_donor";
            PreparedStatement stmt = conn.initStatement(sql);
            ResultSet rst = stmt.executeQuery();
            while (rst.next()) {
                String donor = rst.getString("Name");
                cmbDonorName.addItem(donor);
            }

        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showConfirmDialog(null, e);
        }
    }

    private void FillBloodCombo() {
        try {
            conn.open();
            String sql = "Select * from tbl_blood_type";
            PreparedStatement stmt = conn.initStatement(sql);
            ResultSet rst = stmt.executeQuery();
            while (rst.next()) {
                String blood = rst.getString("Blood_Group");
                cmbBloodType.addItem(blood);
            }

        } catch (ClassNotFoundException | SQLException e) {
            JOptionPane.showConfirmDialog(null, e);
        }
    }

    private void searchDonorbyDID(String name) throws ClassNotFoundException, SQLException {
        DonorBloodService ds = new DonorBloodService();
        Donor existingDonor = ds.searchDonor(name);
        if (existingDonor != null) {
            cmbBloodType.setSelectedItem(existingDonor.getBloodGrp());
        }
    }

    public void clearAllFields() {        
        cmbDonorName.setSelectedIndex(-1);
        cmbBloodType.setSelectedIndex(-1);
        spnBloodPacket.setValue(0);
        donateDateChooser.setDate(null);
    }

