    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
       
            if (Integer.parseInt(spnBloodPacket.getValue().toString())==0)
            {
                JOptionPane.showMessageDialog(this, "At least 1 blood packet must be saved");
            }else if(donateDateChooser.getDate()==null)
            {
                JOptionPane.showMessageDialog(this, "Please enter the date");
            }
            else
            {
                try {
                    java.util.Date donatedDate = donateDateChooser.getDate();
                    DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
                    String datedon = df.format(donatedDate);
                    java.sql.Date sqldon= new java.sql.Date(donatedDate.getTime());                    
                    DonorBloodService donorBloodService = new DonorBloodService();
                    DonorBlood donorBlood = new DonorBlood();
                    donorBlood.setDonorName(cmbDonorName.getSelectedItem().toString());
                    donorBlood.setBloodGrp(cmbBloodType.getSelectedItem().toString());
                    donorBlood.setBloodPacket(Integer.parseInt(spnBloodPacket.getValue().toString()));
                    donorBlood.setDonotedDate(sqldon);
                    donorBloodService.insert(donorBlood);
                    JOptionPane.showMessageDialog(this, "Blood packets Added Successfully", "Add Blood Packets", JOptionPane.INFORMATION_MESSAGE);
                    clearAllFields();
                } catch (ClassNotFoundException | SQLException ex) {
                    Logger.getLogger(BloodGroupsForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            
        } 
        
           
        
    

    }//GEN-LAST:event_saveButtonActionPerformed

