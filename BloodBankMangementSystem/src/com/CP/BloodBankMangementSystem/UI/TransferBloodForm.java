    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            if(Integer.parseInt(spnBlood.getValue().toString())==0)
                {
                JOptionPane.showMessageDialog(this, "At least 1 blood packet must be saved");
            }
            else
            {
            HospitalBloodService hospitalbloodService = new HospitalBloodService();
            HospitalBlood hospitalBlood = new HospitalBlood();
            hospitalBlood.setHospitalName(cmbHospital.getSelectedItem().toString());
            hospitalBlood.setPatientName(cmbPatient.getSelectedItem().toString());
            hospitalBlood.setBloodGrp(cmbBlood.getSelectedItem().toString());
            hospitalBlood.setBloodPacket(Integer.parseInt(spnBlood.getValue().toString()));
            hospitalbloodService.insert(hospitalBlood);
            JOptionPane.showMessageDialog(this, "Blood Transferred Successfully", "Transfer Blood", JOptionPane.INFORMATION_MESSAGE);            
            clearAllFields();
            }
        } catch (SQLException ex) {
            Logger.getLogger(TransferBloodForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TransferBloodForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_saveButtonActionPerformed

