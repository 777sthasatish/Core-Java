    private void FillComboHospital() throws SQLException, ClassNotFoundException
    {
        DBConnection conn=new DBConnection();
        conn.open();
        String sql="Select * from tbl_patient";
        
        PreparedStatement stmt=conn.initStatement(sql);
        ResultSet rst = stmt.executeQuery();
        while (rst.next()) {
                String name = rst.getString("Hospital_Name");
                cmbHospital.addItem(name);
            }
            conn.close();      
    }
    private void FillComboPatient() throws SQLException, ClassNotFoundException
    {
        DBConnection conn=new DBConnection();
        conn.open();
        String sql="Select * from tbl_patient";
        
        PreparedStatement stmt=conn.initStatement(sql);
        ResultSet rst = stmt.executeQuery();
        while (rst.next()) {
                String name = rst.getString("Name");                
                cmbPatient.addItem(name);
            }
            conn.close();      
    }
    private void searchPatientName(String name) throws ClassNotFoundException, SQLException {
        HospitalBloodService hb = new HospitalBloodService();
        Patient existingPatient = hb.searchPatient(name);
        if (null != existingPatient) {            
            
            cmbBlood.setSelectedItem(existingPatient.getBloodGrp());
            
        }
    }    
    private void FillBloodCombo() throws ClassNotFoundException, SQLException
    {
        DBConnection conn=new DBConnection();
        conn.open();
        String sql="Select * from tbl_blood_type";
        
        PreparedStatement stmt=conn.initStatement(sql);
        ResultSet rst = stmt.executeQuery();
        while (rst.next()) {
                String blood = rst.getString("Blood_Group");
                cmbBlood.addItem(blood);
            }
            conn.close();
    }
     public void clearAllFields() {

        cmbHospital.setSelectedIndex(-1);
        cmbPatient.setSelectedIndex(-1);
        cmbBlood.setSelectedIndex(-1);
        spnBlood.setValue(0);
        
    }
