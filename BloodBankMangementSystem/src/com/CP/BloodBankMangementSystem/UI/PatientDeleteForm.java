    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            PatientService patientService = new PatientService();
            int pid = cmbPid.getSelectedIndex();
        int showConfirmDialog = JOptionPane.showConfirmDialog(this, "Are you sure that you want to delete p " + pid);
        if (showConfirmDialog == 0) {
            boolean deleted = patientService.deletePatient(pid);
            if (deleted =true) {
                JOptionPane.showMessageDialog(null, "Deleted Succesfully");
                clearAllFields();
            } else {
                JOptionPane.showMessageDialog(null, "Error while Deleting!");
                clearAllFields();
            }
        }
        } catch (ClassNotFoundException | SQLException ex) {
           JOptionPane.showMessageDialog(this, ex);
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

